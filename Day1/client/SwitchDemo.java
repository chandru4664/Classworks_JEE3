package com.htc.Day1.client;

public class SwitchDemo {

	public static void main(String[] args) {
		String color="green";
		switch(color)
		{
		case "green":
			System.out.println("Go ahead");
			break;
		case "red":
			System.out.println("Stop!!!");
			break;
		case "amber":
			System.out.println("Get ready");
			break;
			
		}
				
		System.out.println("end of program");		
		
	}
}
